
public class Model {

    private String top, bottom;

    public Model() {
        this.top = "";
        this.bottom = "";
    }

    public String top() {
        return this.top;
    }

    public String bottom() {
        return this.bottom;
    }

    public void setTop(String set) {
        this.top = set;
    }

    public void setBottom(String set) {
        this.bottom = set;
    }
}
